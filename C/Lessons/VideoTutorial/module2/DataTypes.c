/* Data Types */
#include <stdio.h>
#include <math.h>

int main() {
    printf("%d", 5 + 4); /* Use %d for int*/
    printf("\n");
    printf("%f", 5.0 + 4.0); /* Use %f for float*/
    printf("\n");
    printf("%f", pow(2, 3) );
    printf("\n");
    printf("%f", sqrt(25) );
    printf("\n");
    printf("%f", ceil(36.356) );
    printf("\n");
    printf("%f", floor(36.356) );
    printf("\n");

    return 0;
}